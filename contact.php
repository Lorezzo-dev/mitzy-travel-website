  document.addEventListener('DOMContentLoaded', () => {
    // --- Keep your original smooth scroll behavior ---
    const status = document.querySelector('.form-status');
    if (status) {
      status.scrollIntoView({ behavior: 'smooth', block: 'center' });
    }

    // --- ✅ Google Analytics tracking: detect success redirect ---
    const urlParams = new URLSearchParams(window.location.search);
    if (urlParams.get('status') === 'success') {
      // Wait for GA tag to be ready, then send event
      setTimeout(() => {
        if (typeof gtag === 'function') {
          gtag('event', 'form_submit', {
            'event_category': 'Contact',
            'event_label': 'General Inquiry'
          });
          console.log('GA4 Event Sent: form_submit');
        }
      }, 1000);
    }
  });

  // --- ✅ reCAPTCHA callback ---
  function onSubmit(token) {
    const form = document.querySelector('#contact-form form');

    // Add hidden token manually before submitting (ensures backend sees it)
    const recaptchaInput = document.createElement('input');
    recaptchaInput.type = 'hidden';
    recaptchaInput.name = 'g-recaptcha-response';
    recaptchaInput.value = token;
    form.appendChild(recaptchaInput);

    // Submit the form normally — backend will redirect to ?status=success
    form.submit();
  }
</script>
