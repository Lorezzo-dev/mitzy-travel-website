  <h1>Japan Visa Requirements</h1>
  <h2>Tourist Visa – Single Entry or Multiple Entry</h2>
  <p>All documents must be <b>original</b> unless otherwise stated, and printed on <b>A4 size paper.</b></p>

  <h3>General Requirements</h3>
  <ol>
    <li><b>Philippine Passport</b> – Valid for at least 6 months, signed on page 3. If previously issued a Japan visa, include the used passport.</li>
    <li><b>Visa Application Form</b> – No erasures or blanks; use “N/A” if not applicable. Applicants under 12 must have the form signed by a parent.</li>
    <li><b>2 pcs. Passport-size Photo</b> – White background, high-resolution (minimum 600 dpi). Write your full name and date of birth at the back.</li>
    <li><b>Agency Authorization Letter</b> – Required for both adult and minor applicants; parents must sign for those under 17.</li>
    <li><b>Japan Embassy Authorization Letter for PSA</b></li>
    <li><b>PSA Birth Certificate</b> – Issued within 1 year (2024–2025).  
      <ul>
        <li>If <b>late registered</b>: Submit Baptismal Certificate and Form 137 or an Explanation Letter (issued within 3 months, with contact info).</li>
        <li>If <b>unreadable</b>: Provide Birth Certificate from Local Civil Registry.</li>
        <li>If <b>no record</b>: Submit Birth Certificate from Local Civil Registry + Negative Certificate from PSA.</li>
      </ul>
    </li>
    <li><b>PSA Marriage Certificate</b> – Issued within 1 year (2024–2025).  
      <ul>
        <li>If unreadable: Submit Marriage Certificate from Local Civil Registry.</li>
        <li>If no record: Marriage Certificate + Negative Certificate from PSA.</li>
      </ul>
    </li>
    <li><b>Multiple Entry Request Form</b> (Optional)</li>
    <li><b>Daily Schedule / Itinerary in Japan</b> – Include arrival/departure dates, flights, accommodation details, and daily activities.</li>
  </ol>

  <h3>For Employed Applicants</h3>
  <ol>
    <li><b>Certificate of Employment</b> – Must include position, date hired, and compensation (original with wet signature).</li>
    <li><b>Approved Leave of Absence</b> – For government employees (original with wet signature).</li>
    <li><b>Bank Certificate</b> – Must include account name, number, type, opening date, current balance, and 6-month ADB. If no ADB, include 6-month Bank Statement. (Validity: 6 months from issuance.)</li>
    <li><b>Income Tax Return (Form 2316)</b> – Latest copy, signed by both employer and employee. If unavailable, submit a Letter of Explanation.</li>
  </ol>

  <h3>For Business Owners / Self-Employed</h3>
  <ol>
    <li><b>DTI or SEC Registration</b> – Clear photocopy of all pages (include stockholders if SEC). Must be updated for the current year.</li>
    <li><b>Business Permit / Mayor’s Permit</b> – Updated copy for the present year.</li>
    <li><b>Bank Certificate</b> – Same financial details as above; if no ADB, attach Bank Statement (6 months). Validity: 6 months.</li>
    <li><b>Income Tax Return (Form 1701)</b> – Latest full copy.  
      <ul>
        <li>Must have BIR receipt stamp or proof of electronic payment.</li>
        <li>Include email confirmation if payment done online.</li>
      </ul>
    </li>
  </ol>

  <h3>If a Guarantor in the Philippines Will Shoulder Travel Expenses</h3>
  <ol>
    <li><b>Guarantee Letter Form</b> – If multiple applicants share a guarantor, list all names on a separate attachment.</li>
    <li><b>Proof of Relationship</b> – Birth/Marriage Certificate, photos, or Certificate of Employment (if company-sponsored).</li>
    <li><b>Guarantor’s Bank Certificate</b> – Original copy.</li>
    <li><b>Guarantor’s ITR (Form 2316/1701/1702)</b> – Latest copy or Explanation Letter if unavailable.</li>
    <li><b>Guarantor’s Employment Certificate or Business Permit</b></li>
  </ol>

  <h3>Students</h3>
  <ul>
    <li>Certificate of Enrollment or School ID copy.</li>
  </ul>

  <h3>Senior Citizens</h3>
  <ul>
    <li>Copy of Senior Citizen ID.</li>
  </ul>

  <h3>OFWs (Overseas Filipino Workers)</h3>
  <ol>
    <li>Certificate of Employment / Contract.</li>
    <li>Residence ID.</li>
    <li>Letter of Explanation for missing ITR (if applicable).</li>
  </ol>

  <h3>For Foreign Residents (Non-Filipino Nationals)</h3>
  <ul>
    <li>Copy of Alien Certificate of Registration (ACR) issued by the Philippine government.</li>
  </ul>

  <h3>Additional Reminder</h3>
  <p>If the applicant already has a used Japan visa, PSA Birth/Marriage Certificates are not required.  
  However, if applying under a guarantor, the guarantor must still provide documents as proof of relationship.</p>

  <p>Applicants unable to provide their own financial documents (Bank Cert, ITR) must include PSA proof of relationship (copy acceptable if used Japan visa is shown on passport).</p>
